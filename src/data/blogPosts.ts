export const getFeaturedPosts = (): BlogPostType[] => {
  return blogPosts.filter((post) => post.isFeatured && post.isPublished);
};

export const getPublishedPosts = (): BlogPostType[] => {
  return blogPosts
    .filter((post) => post.isPublished)
    .sort(
      (a, b) =>
        new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
    );
};

export const getPostBySlug = (slug: string): BlogPostType | undefined => {
  return blogPosts.find((post) => post.slug === slug && post.isPublished);
};

export const getPostsByCategory = (category: string): BlogPostType[] => {
  return blogPosts.filter(
    (post) => post.category === category && post.isPublished
  );
};
